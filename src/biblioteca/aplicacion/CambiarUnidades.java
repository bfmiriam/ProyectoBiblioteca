    private void buscarSiguienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_buscarSiguienteMouseClicked
        // TODO add your handling code here:
        if (!isbnLibro.getText().equalsIgnoreCase("")){
            Ejemplar ejLibro = new Ejemplar();
            try{
                ArrayList ejemplares = ejLibro.crearArrayISBN(isbnLibro.getText());
                Libro lib = (Libro)ejemplares.get(0);
                mas.setVisible(true);
                menos.setVisible(true);
                unidades.setVisible(true);
                guardar.setVisible(true);
                unidades.setText(String.valueOf(lib.getNumUnidades()));
            }catch(IndexOutOfBoundsException ex){
                Biblioteca.mostrarMensaje("El ISBN introducido no pertenece a ninguno de los libros de la biblioteca");
            }
        }else{
            try {
                throw new ExcepcionTextoBlanco("No has introducido ningÃºn ISBN. Introduce uno para continuar");
            } catch (ExcepcionTextoBlanco ex) {
                Biblioteca.mostrarMensaje(ex.getMessage());
            }
        }
    }//GEN-LAST:event_buscarSiguienteMouseClicked

    private void masMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_masMouseClicked
        // TODO add your handling code here:
        int numUnidades = Integer.parseInt(unidades.getText())+1;
        unidades.setText(String.valueOf(numUnidades));
    }//GEN-LAST:event_masMouseClicked

    private void menosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_menosMouseClicked
        // TODO add your handling code here:
        int numUnidades = Integer.parseInt(unidades.getText())-1;
        if (numUnidades<0){
            int opcion = Biblioteca.comprobacion("El numero de unidades es 0.\nÂ¿Desea eliminar el libro?");
            if (opcion==0){
                this.setVisible(false);
                new EliminarLibro().setVisible(true);
            }
        }else{
            unidades.setText(String.valueOf(numUnidades));
        } 
    }//GEN-LAST:event_menosMouseClicked

    private void guardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_guardarMouseClicked
        // TODO add your handling code here:
        Ejemplar ejLibro = new Ejemplar();
        try {
            ejLibro.cambiarUnidades(isbnLibro.getText(),Integer.parseInt(unidades.getText()));
            this.setVisible(false);
            new Libros().setVisible(true);
        } catch (IOException ex) {
            Logger.getLogger(CambiarUnidades.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_guardarMouseClicked

